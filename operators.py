print(x + Y)
print(x - Y)
print(x * Y)
print(x / Y)
print(x % Y) # returns the reminder

#compare the operators
print(x< Y)
print(x > Y)
print(x<= Y)
print(x>= Y)
print(x== Y)
print(x!= Y)

